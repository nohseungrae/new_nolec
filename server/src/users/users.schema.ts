import * as mongoose from 'mongoose';
//MongoDB 에 들어갈 Data의 형태 정의 ---(mongoose)
export const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
  },
});
